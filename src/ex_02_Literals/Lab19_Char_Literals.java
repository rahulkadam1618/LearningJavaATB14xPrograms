package ex_02_Literals;

public class Lab19_Char_Literals {
    public static void main(String[] args) {
        char c1 = 'C';
        char c11 = '9';
        char c2 = '@';
        char c3 = '%';
/*    Escape Char
    \n - new line \t - tab line \b - back space \r - carriage return*/
        System.out.println("Hi\nSir");
//        new line
        System.out.println("Hi\tSir");
//        space b/w 2 words
        System.out.println("Hi\bSir");
//        backspace one letter removed
        System.out.println("Hi\rSir");
//        Sir




    }
}
